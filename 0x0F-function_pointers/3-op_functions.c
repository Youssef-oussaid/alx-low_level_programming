#include "3-calc.h"

/**
 * op_add - adds
 * @a: first
 * @b: second
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculates
 * @a: first
 * @b: second
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calculates
 * @a: first
 * @b: second
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calculates
 * @a: first
 * @b: second
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculates
 * @a: first
 * @b: second
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
